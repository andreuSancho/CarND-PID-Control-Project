#ifndef PID_H
#define PID_H

class PID {
public:
	/**
	* Constants used.
	*/
	static const double KP_DEFAULT;
	static const double KI_DEFAULT;
	static const double KD_DEFAULT;
	static const double INITIAL_ERROR;

  /*
  * Errors
  */
  double p_error;
  double i_error;
  double d_error;

  /*
  * Coefficients
  */ 
  double Kp;
  double Ki;
  double Kd;

  /*
  * Constructor
  */
  PID();

  /*
  * Destructor.
  */
  virtual ~PID();

  /*
  * Initialize PID.
  */
  void Init(double Kp, double Ki, double Kd);

  /*
  * Update the PID error variables given cross track error.
  */
  void UpdateError(double cte);

  /*
  * Calculate the total PID error.
  */
  double TotalError();
};

#endif /* PID_H */
